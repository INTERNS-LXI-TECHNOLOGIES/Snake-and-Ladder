public abstract class SnakeOrLadder{
	

	
	protected String name;
	protected int start;
	protected int end;

	//Getters and Setters
	public String getName(){
		return name;
	}
	public void setName(String name){
		this.name=name;
	}
	public int getStart(){
		return start;
	}
	public void setStart(int start){
		this.start=start;
	}
	public int getEnd(){
		return end;
	}
	public void setEnd(int end){
		this.end=end;
	}	
	
}