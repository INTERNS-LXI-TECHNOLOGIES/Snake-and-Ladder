package com.lxisoft.snakeandladder;
public class Ladder
{
  private int bottom;
  private int top;
  public void setBottom(int bottom)
  {
	  this.bottom = bottom;
  }  
  public int getBottom()
  {
	  return bottom;
  }
  public void setTop(int top)
  {
	  this.top = top;
  }
  public int getTop()
  {
	  return top;
  }
}